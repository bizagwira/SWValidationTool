$('.drag').draggable({ 
  appendTo: 'body',
  helper: 'clone'
});

$('#dropzone').droppable({
  activeClass: 'active',
  hoverClass: 'hover',
  accept: ":not(.ui-sortable-helper)", // Reject clones generated by sortable
  drop: function (e, ui) {
    var $row = $('<div class="panel drop-item"></div>');
    var $heading = $('<div class="panel-heading"></div>');

    $heading.append($('<a href="#" class="btn btn-xs btn-raised pull-right btn-xs remove">\
        <i class="glyphicon glyphicon-trash" aria-hidden="true"> </i>\
    </a>')
    .click(function () {$(this).parent().parent().detach(); }));

    $heading.append($('<a href="#"><i aria-hidden="true">' + ui.draggable.text() + '</i> </a>'));
    $row.append($heading);
    $(this).append($row);

  }
}).sortable({
  items: '.drop-item',
  sort: function() {
    // gets added unintentionally by droppable interacting with sortable
    // using connectWithSortable fixes this, but doesn't allow you to customize active/hoverClass options
    $( this ).removeClass( "active" );
  }
});